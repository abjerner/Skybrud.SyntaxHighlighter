namespace Skybrud.SyntaxHighlighter {

    /// <summary>
    /// Enum class representing the supported languages of this package.
    /// </summary>
    public enum Language {

        /// <summary>
        /// Indiciates that a language isn't specified.
        /// </summary>
        None,

        /// <summary>
        /// Indicates that the language is <strong>C#</strong>.
        /// </summary>
        CSharp,

        /// <summary>
        /// Indicates that the language is <strong>JSON</strong>.
        /// </summary>
        Json,

        /// <summary>
        /// Indicates that the language is <strong>JavaScript</strong>.
        /// </summary>
        JavaScript,

        /// <summary>
        /// Indicates that the language is <strong>HTML</strong>.
        /// </summary>
        Html,

        /// <summary>
        /// Indicates that the language is <strong>XML</strong>.
        /// </summary>
        Xml,

        /// <summary>
        /// Indicates that the language is <strong>NuGet</strong>.
        /// </summary>
        NuGet

    }

}